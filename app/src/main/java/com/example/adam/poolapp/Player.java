package com.example.adam.poolapp;

class Player {
    int id;
    String first_name;
    String last_name;
    int teamId;

    public Player(int id, String first_name, String last_name, int teamId) {
        this.id = id;
        this.first_name = first_name;
        this.last_name = last_name;
        this.teamId = teamId;
    }
}
